CREATE VIEW `dashboard_stats` AS
SELECT
  (SELECT COUNT(*) FROM `locations` WHERE `locations`.`is_active` = 1) AS `active_locations`,
  (SELECT COUNT(*) FROM `locations`) AS `total_locations`,
  (SELECT COUNT(*) FROM `vehicles` WHERE (`vehicles`.`is_available` = 1) AND (`vehicles`.`is_active` = 1)) AS `available_vehicles`,
  (SELECT COUNT(*) FROM `vehicles` WHERE `vehicles`.`is_active` = 1) AS `active_vehicles`,
  (SELECT COUNT(*) FROM `vehicles`) AS `total_vehicles`,
  (SELECT COUNT(*) FROM `tours` WHERE `tours`.`is_active` = 1) AS `total_tours`,
  (SELECT COUNT(*) FROM `assignments` WHERE (`assignments`.`assignment_date` = CURDATE()) AND (`assignments`.`status` = 'confirmed')) AS `today_assignments`,
  (SELECT COUNT(*) FROM `qr_scan_logs` WHERE (CAST(`qr_scan_logs`.`scan_timestamp` AS DATE) = CURDATE())) AS `today_qr_scans`;

